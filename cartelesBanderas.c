void DiagramaFinal (fase arregloDeFases[],stPartido nuevoArregloDePartidos[16], int* validos)
{
    ///
    nodoPartido* partidosDeoctavos = arregloDeFases[0].partidos;
    nodoPartido* partidosDeCuartos = arregloDeFases[1].partidos;
    nodoPartido* partidosSemis = arregloDeFases[2].partidos;
    nodoPartido* partido3erPuesto = arregloDeFases[3].partidos;
    nodoPartido* partidoFinal = arregloDeFases[4].partidos;

    while(partidosDeoctavos)
    {
        insertarEnArregloDiagrama (partidosDeoctavos,nuevoArregloDePartidos,validos);
        partidosDeoctavos = partidosDeoctavos->sig;
    }

    while(partidosDeCuartos)
    {
        insertarEnArregloDiagrama(partidosDeCuartos,nuevoArregloDePartidos,validos);
        partidosDeCuartos = partidosDeCuartos->sig;
    }

    while(partidosSemis)
    {
        insertarEnArregloDiagrama(partidosSemis,nuevoArregloDePartidos,validos);
        partidosSemis = partidosSemis->sig;
    }

    if(partido3erPuesto)
    {
        insertarEnArregloDiagrama(partido3erPuesto,nuevoArregloDePartidos,validos);
    }
    if(partidoFinal)
    {
        insertarEnArregloDiagrama(partidoFinal,nuevoArregloDePartidos,validos);
    }

///A esta altura, el arreglo de partidos esta totalmente cargado.
    partidosToDiagrama (nuevoArregloDePartidos);

}


